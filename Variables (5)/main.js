// Variables are containers for storing data (storing data values).
// You can declare a JavaScript variable using 'var', 'let', 'const' or using nothing. Examples:
var x = 5;
var y = 6;
var z = x + y;

let a = 1;
let b = 2;
let c = a + b;

i = 8;
j = 9;
k = i + j;

// When to Use JavaScript const?
// If you want a general rule: always declare variables with const. If you think the value of the variable can change, use let. Exmaple:
const price1 = 30;
const price2 = 50;
let total = price1 + price2;

// Declaring a JavaScript variable
var carName;
// To assign a value to the variable, use the equal sign:
carName = "Audi"
